      fprintf(errorFile,"Undeclared identifier.\n");
      break;
    case 12:
      fprintf(errorFile,"Assignment to constant or procedure is not allowed\n");
      break;
    case 13:
      fprintf(errorFile,"Assignment operator expected.\n");
